@dataclass
class ProcessingResult:
    """Result of data processing operation"""
    id: str
    status: str
    data: Dict[str, Any]
    timestamp: datetime
    error: Optional[str] = None

class DataProcessor:
    """Main data processing class"""
    
    def __init__(self, config: Dict[str, Any]):
        self.config = config
        self.results = []
        
    def validate_input(self, data: Dict[str, Any]) -> bool:
        """Validate input data structure"""
        required_fields = ['id', 'type', 'content']
        
        for field in required_fields:
            if field not in data:
                return False
        
        return True
    
    def process_item(self, item: Dict[str, Any]) -> ProcessingResult:
        """Process a single data item"""
        if not self.validate_input(item):
            return ProcessingResult(
                id=item.get('id', 'unknown'),
                status='error',
                data={},
                timestamp=datetime.now(),
                error='Invalid input format'
            )
        
        try:
            # Processing logic (slightly different approach)
            processed_data = {
                'original_id': item['id'],
                'type': item['type'],
                'processed_content': self.transform_content(item['content']),
                'metadata': self.extract_metadata(item)
            }
            
            result = ProcessingResult(
                id=item['id'],
                status='success',
                data=processed_data,
                timestamp=datetime.now()
            )
            
            self.results.append(result)
            return result
            
        except Exception as e:
            return ProcessingResult(
                id=item['id'],
                status='error',
                data={},
                timestamp=datetime.now(),
                error=str(e)
            )
    
    def transform_content(self, content: str) -> str:
        """Transform content data"""
        # Slightly different transformation approach
        transformed = content.strip().lower()  # Different case handling
        
        # Apply filters
        if len(transformed) > 100:
            transformed = transformed[:100] + "..."
        
        return transformed
    
    def extract_metadata(self, item: Dict[str, Any]) -> Dict[str, Any]:
        """Extract metadata from item"""
        metadata = {
            'length': len(item.get('content', '')),
            'has_special_chars': any(c in item.get('content', '') for c in '!@#$%'),
            'word_count': len(item.get('content', '').split()),
            'char_count': len(item.get('content', ''))  # Additional field
        }
        
        return metadata
    
    def process_batch(self, items: List[Dict[str, Any]]) -> List[ProcessingResult]:
        """Process multiple items"""
        results = []
        
        for item in items:
            result = self.process_item(item)
            results.append(result)
        
        return results
    
    def get_summary(self) -> Dict[str, Any]:
        """Get processing summary"""
        total = len(self.results)
        successful = len([r for r in self.results if r.status == 'success'])
        failed = total - successful
        
        return {
            'total_processed': total,
            'successful': successful,
            'failed': failed,
            'success_rate': successful / total if total > 0 else 0.0,
            'avg_processing_time': 0.15  # Additional metric
        }

